export function translateSkill(skill: string, lang: string): string {
  if (lang === 'en') return skill;
  if (SKILLS_MAP[skill]?.[lang]) return SKILLS_MAP[skill][lang];
  const key = Object.keys(SKILLS_MAP).find(k => k.toLowerCase() === skill.toLowerCase());
  if (key && SKILLS_MAP[key]?.[lang]) return SKILLS_MAP[key][lang];
  return skill;
}

export function translateJobTitle(title: string, lang: string): string {
  if (!title || lang === 'en') return title;

  // Exact match (case-insensitive)
  const exactKey = Object.keys(JOB_TITLES_MAP).find(k => k.toLowerCase() === title.toLowerCase().trim());
  if (exactKey && JOB_TITLES_MAP[exactKey]?.[lang]) return JOB_TITLES_MAP[exactKey][lang];

  // If contains slash, translate each part
  if (title.includes('/')) {
    return title.split('/').map(part => translateJobTitle(part.trim(), lang)).join(' / ');
  }

  // No match found — return ORIGINAL text, never partial replace
  return title;
}

export function translateEducation(education: string, lang: string): string {
  if (lang === 'en') return education;
  let result = education;
  for (const [key, translations] of Object.entries(DEGREES_MAP)) {
    if (result.includes(key) && translations[lang]) {
      result = result.replace(key, translations[lang]);
    }
  }
  return result;
}

export function translateCertification(cert: string, lang: string): string {
  if (lang === 'en') return cert;
  if (CERTIFICATIONS_MAP[cert]?.[lang]) return CERTIFICATIONS_MAP[cert][lang];
  const key = Object.keys(CERTIFICATIONS_MAP).find(k => k.toLowerCase() === cert.toLowerCase());
  if (key && CERTIFICATIONS_MAP[key]?.[lang]) return CERTIFICATIONS_MAP[key][lang];
  return cert;
}

export function translateExperience(years: number, lang: string): string {
  if (lang === 'he') return `${years} שנים`;
  if (lang === 'tl') return `${years} taon`;
  return `${years} years`;
}
